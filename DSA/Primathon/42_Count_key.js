//JavaScript Program to Count the Number of Keys/Properties in an Object

const person = { 
    name: 'John',
    age: 20,
    hobbies: ['reading', 'games', 'coding'],
    greet: function() {
        console.log('Hello everyone.');
    },
    score: {
        maths: 90,
        science: 80
    }
};

const count = Object.keys(person).length;
console.log(count);